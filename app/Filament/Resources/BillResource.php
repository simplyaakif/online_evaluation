<?php

namespace App\Filament\Resources;

use App\Filament\Resources\BillResource\Pages;
use App\Filament\Resources\BillResource\RelationManagers;
use App\Models\Bill;
use Filament\Forms;
use Filament\Resources\Form;
use Filament\Resources\Resource;
use Filament\Resources\Table;
use Filament\Tables;
use Filament\Tables\Columns\TextColumn;
use Filament\Tables\Filters\Layout;
use Filament\Tables\Filters\SelectFilter;
use Illuminate\Database\Eloquent\Builder;
use Illuminate\Database\Eloquent\SoftDeletingScope;

class BillResource extends Resource
{
    protected static ?string $model = Bill::class;

    protected static ?string $navigationIcon = 'heroicon-o-receipt-tax';

    protected static ?string $navigationGroup = 'Invoice';

    public static function form(Form $form): Form
    {
        return $form
            ->schema([
                Forms\Components\TextInput::make('id'),
                Forms\Components\Textarea::make('amount')
                    ->maxLength(255),
                Forms\Components\DatePicker::make('due_date'),
                Forms\Components\Textarea::make('status')
                    ->maxLength(255),
                Forms\Components\DatePicker::make('paid_on'),
                Forms\Components\TextInput::make('candidate_id'),
                Forms\Components\TextInput::make('course_id'),
                Forms\Components\TextInput::make('candidate_course_id'),
                Forms\Components\Textarea::make('pay_link')
                    ->maxLength(65535),
                Forms\Components\Textarea::make('bill_url')
                    ->maxLength(65535),
                Forms\Components\Textarea::make('pay_id')
                    ->maxLength(65535),
            ]);
    }

    public static function table(Table $table): Table
    {
        return $table
            ->columns([
                TextColumn::make('candidate.name')->label('Name')->searchable(),
                TextColumn::make('candidate_course.course_name')->label('Course'),
                TextColumn::make('amount')->suffix(' Rs'),
                TextColumn::make('candidate_course.campus')->label('Campus')->searchable(),
                TextColumn::make('candidate_course.course_mode')->label('Mode of Learning')->searchable(),
                TextColumn::make('candidate_course.course_duration')->label('Duration'),
                TextColumn::make('candidate_course.course_time')->label('Time'),
                TextColumn::make('candidate_course.course_start_date')->label('Start Date'),
                TextColumn::make('due_date')
                    ->date(),
                TextColumn::make('status'),
                TextColumn::make('paid_on')
                    ->date(),
                TextColumn::make('created_at')
                    ->dateTime()
                ->sortable(),
            ])->defaultSort('created_at','desc')
            ->filters([

            ])
            ->actions([
//                Tables\Actions\EditAction::make(),
            ])
            ->bulkActions([
                Tables\Actions\DeleteBulkAction::make(),
            ]);
    }

    public static function getRelations(): array
    {
        return [
            //
        ];
    }

    public static function getPages(): array
    {
        return [
            'index' => Pages\ListBills::route('/'),
            'create' => Pages\CreateBill::route('/create'),
            'edit' => Pages\EditBill::route('/{record}/edit'),
        ];
    }

    public static function canCreate(): bool
    {
        return false; // TODO: Change the autogenerated stub
    }
}
